  // ---------- FUNÇÕES DO LIGHTBOX ----------
  function atualizarLightboxMedia(src) {
    if (src.match(/\.(mp4|webm|mov|ogg)$/i)) {
      lightboxImg.style.display = 'none';
      lightboxVideo.style.display = 'block';
      lightboxVideo.src = src;
      lightboxVideo.load();
      lightboxVideo.play();
    } else {
      lightboxVideo.style.display = 'none';
      lightboxImg.style.display = 'block';
      lightboxImg.src = src;
    }
  }

  function abrirLightbox(src, mediaList, index) {
    // Esconde ambos
    lightboxImg.style.display = 'none';
    lightboxVideo.style.display = 'none';

    // Mostra o tipo correto
    if (src.match(/\.(mp4|webm|mov|ogg)$/i)) {
      lightboxVideo.src = src;
      lightboxVideo.style.display = 'block';
      lightboxVideo.load();
      lightboxVideo.play();
    } else {
      lightboxImg.src = src;
      lightboxImg.style.display = 'block';
    }

    currentMediaList = mediaList;
    currentMediaIndex = index;

    lightboxTitle.textContent = '';
    lightboxDesc.textContent = '';
    lightbox.classList.remove('hidden');
    body.classList.add('locked');
    }

    function fecharLightbox() {
    lightbox.classList.add('hidden');
    body.classList.remove('locked');
    lightboxVideo.pause();
    lightboxVideo.src = '';
    currentMediaList = [];
    }

    if (prevBtn) {
      prevBtn.addEventListener('click', (e) => {
        e.stopPropagation();
          if (currentMediaList.length > 0) {
        currentMediaIndex = (currentMediaIndex - 1 + currentMediaList.length) % currentMediaList.length;
        atualizarLightboxMedia(currentMediaList[currentMediaIndex]);
      }
    });
    }

    if (nextBtn) {
      nextBtn.addEventListener('click', (e) => {
        e.stopPropagation();
          if (currentMediaList.length > 0) {
        currentMediaIndex = (currentMediaIndex + 1) % currentMediaList.length;
        atualizarLightboxMedia(currentMediaList[currentMediaIndex]);
      }
    });
  }

    if (closeBtn) {
      closeBtn.addEventListener('click', (e) => {
        e.stopPropagation();
        fecharLightbox();
      });
    }

    lightbox.addEventListener('click', (e) => {
      if (e.target === lightbox) {
        fecharLightbox();
      }
    });

    function abrirModal(proj) {
      let imagensHTML = '';
      proj.imagens.forEach((img, index) => {
        if (img.match(/\.(mp4|webm|mov|ogg)$/i)) {
          imagensHTML += `<div class="galeria-item-video"><video autoplay loop muted src="${img}" alt="Imagem ${index+1} - ${proj.nome}"></video></div>`;
        } else {
          imagensHTML += `<div class="galeria-item"><img src="${img}" alt="Imagem ${index+1} - ${proj.nome}" loading="lazy"></div>`;
        }
      });

    const context = proj.contexto ? `<div class="bloco"><h2>Contexto</h2><p>${proj.contexto}</p></div>` : '';
    const problema = proj.problema ? `<div class="bloco"><h2>Problema</h2><p>${proj.problema}</p></div>` : '';
    const conceito = proj.conceito ? `<div class="bloco"><h2>Conceito</h2><p>${proj.conceito}</p></div>` : '';
    const decisoes = proj.decisoes ? `<div class="bloco"><h2>Decisões visuais</h2><p>${proj.decisoes}</p></div>` : '';
    const resultado = proj.resultado ? `<div class="resultado"><p><strong>Resultado</strong> — ${proj.resultado || 'Não informado'}</p></div>` : '';
    const html = `
      <button class="modal-fechar" onclick="fecharModal()">&times;</button>
      <div class="projeto-cabecalho">
        <h1>${proj.nome}</h1>
        <div class="projeto-meta">
          <span><strong>Categoria:</strong> ${proj.categoria}</span>
          <span><strong>Ano:</strong> ${proj.ano}</span>
          <span><strong>Ferramentas:</strong> ${proj.ferramentas}</span>
        </div>
      </div>
      ${context}  
      ${problema}
      ${conceito}
      ${decisoes}
      <div class="galeria-projeto">
        <h2>Galeria</h2>
        <div class="galeria-grid">${imagensHTML}</div>
      </div>
      ${resultado}
    `;

    modalConteudo.innerHTML = html;
    overlay.classList.add('active');
    body.classList.add('locked');

    const imagensGaleria = modalConteudo.querySelectorAll('.galeria-item img, .galeria-item-video video');
    imagensGaleria.forEach((item, idx) => {
      item.addEventListener('click', (e) => {
        e.stopPropagation();
        let src = item.tagName === 'IMG' ? item.src : item.currentSrc;
        abrirLightbox(src, proj.imagens, idx);
      });
    });
    }

    window.fecharModal = function() {
      overlay.classList.remove('active');
      body.classList.remove('locked');
    };

    overlay.addEventListener('click', (e) => {
      if (e.target === overlay) fecharModal();
    });

    document.addEventListener('keydown', (e) => {
      if (e.key === 'Escape' && overlay.classList.contains('active')) fecharModal();
    });

    function renderCards() {
      grid.innerHTML = '';
      projetos.forEach(proj => {
        const card = document.createElement('div');
        card.className = 'projeto-card';
        card.dataset.id = proj.id;
        card.innerHTML = `
          <img src="${proj.imagemThumb}" alt="${proj.nome}" class="card-imagem" loading="lazy">
          <div class="card-info">
            <h3>${proj.nome}</h3>
            <span class="card-categoria">${proj.categoria}</span>
          </div>
        `;
        card.addEventListener('click', () => abrirModal(proj));
        grid.appendChild(card);
      });
    }

    renderCards();
